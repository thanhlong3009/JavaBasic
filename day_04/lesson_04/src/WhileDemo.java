public class WhileDemo {
    public static void main(String[] args) {
        // thực hiện công việc thức dậy 5h trong vòng 30 ngày
        int day = 1;
        while (day <= 5) {
            System.out.printf("ngày thứ %d thức dậy lúc 5h\n", day);
            day++;
        }

        // Tiết kiệm 100k mỗi ngày trong vòng 1 tháng

    }
}
